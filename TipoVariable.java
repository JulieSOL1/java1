
public class TipoVariable {
  public static void main(String[] arg) {
	  System.out.println("Hola Mundo");
	  
	  int edad = 28;
	  System.out.println(edad);
	  
	  edad = 47;
	  System.out.println(edad);
	  
	  edad = 46 + 88;
	  System.out.println("Mi edad es " + edad);
	  
	  double salario = 1250.56;
	  System.out.println(salario);
	  
	 }
}